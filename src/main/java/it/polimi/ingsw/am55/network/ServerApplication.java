    /**
     * Broadcasts a message to every client registered in the active game.
     *
     * @param message message to deliver to game clients
     */
    @Override
    public void broadcast(MessageToClient message) {
        Map<String, VirtualView> copy;

        synchronized (gameClients) {
            copy = new HashMap<>(gameClients);
        }

        System.out.println("[SERVER_APP] broadcast "
                + message.getClass().getSimpleName()
                + " to clients: "
                + copy.keySet());

        for (Map.Entry<String, VirtualView> entry : copy.entrySet()) {
            String playerId = entry.getKey();
            VirtualView client = entry.getValue();

            try {
                System.out.println("[SERVER_APP] Sending broadcast to: " + playerId);

                client.onMessage(message);

                System.out.println("[SERVER_APP] Broadcast completed for: " + playerId);

            } catch (Exception e) {
                System.out.println("[SERVER_APP] Client " + playerId
                        + " unreachable during broadcast ");

            }
        }
    }

