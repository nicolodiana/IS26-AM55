    /**
     * Handles a voluntary game quit, notifies game and lobby clients, and resets server state.
     *
     * @param playerId player requesting to quit the game
     */
    public void quitGame(String playerId){
        System.out.println("[SERVER_APP] quitGame called by: " + playerId);

        MessageToClient message = controller.quitGame(playerId);
        MessageToClient messageToLobby = new QuitLobbyMessage("One or more players has requested to exit, you're disconnected",true);

        message.deliver(playerId, this);
        messageToLobby.deliver(null,this);

        System.out.println("[SERVER_APP] Quit broadcast completed.");

        resetServer();
    }

    /**
     * Handles a voluntary lobby quit and removes the client associated with the session.
     *
     * @param sessionId lobby session identifier to disconnect
     */
    public void quitLobby(String sessionId){

        MessageToClient message = new QuitLobbyMessage("Ti sei disconnesso dalla lobby",false);
        message.deliver(sessionId, this);

        unregisterLobbyClient(sessionId);

    }
    /**
     * Clears game and lobby network registries, closes known client endpoints, and resets heartbeat state.
     */
    private void resetServer(){
        synchronized (gameClients) {
            for (VirtualView gameClient : gameClients.values()) {
                try {
                    gameClient.close();
                } catch (Exception ignored) {
                }
            }

            gameClients.clear();
            System.out.println("[SERVER_APP] Game client map cleared " + gameClients);
        }

        synchronized (lobbyClients) {
            for (VirtualView lobbyClient : lobbyClients.values()) {
                try {
                    lobbyClient.close();
                } catch (Exception ignored) {
                }
            }

            lobbyClients.clear();
            System.out.println("[SERVER_APP] Lobby client map cleared " + lobbyClients);
        }
        stopAliveChecker();
        synchronized (lastPingByClient) {
            lastPingByClient.clear();
            System.out.println("[SERVER_APP] Ping map cleared " + lastPingByClient);
        }
        if(isGameCrashed.get()) isGameCrashed.set(false);
    }

    /**
     * Updates the heartbeat timestamp for a client and sends a pong response.
     *
     * @param sender client endpoint that sent the ping command
     * @throws Exception if the pong response cannot be delivered
     */
    public void ping(VirtualView sender) throws Exception {
        if (sender == null) {
            return;
        }

        synchronized (lastPingByClient) {
            lastPingByClient.put(sender, System.currentTimeMillis());
        }

        pong(sender);
    }

    /**
     * Sends a pong response to either a game client or a lobby client.
     *
     * @param sender client endpoint that sent the ping
     * @throws Exception if the client identifier cannot be read or the message cannot be delivered
     */
    private void pong(VirtualView sender) throws Exception {
        synchronized (gameClients) {
            String playerId = sender.getPlayerId();

            if (playerId != null && gameClients.containsKey(playerId)) {
                sendTo(playerId, new PongMessage());
                System.out.println("[SERVER_APP] PongMessage game client " + playerId);
                return;
            }
        }

        synchronized (lobbyClients) {
            for (Map.Entry<String, VirtualView> entry : lobbyClients.entrySet()) {
                if (entry.getValue().equals(sender)) {
                    sendToSession(entry.getKey(), new PongMessage());
                    System.out.println("[SERVER_APP] PongMessage lobby client "
                            + sender.getClass().getSimpleName());
                    return;
                }
            }
        }
    }

    /**
     * Starts the server-side heartbeat checker if it is not already running.
     */
    private void startAliveChecker() {
        if (aliveCheckerStarted) {
            return;
        }

        aliveCheckerStarted = true;

        aliveChecker.schedule(new TimerTask() {
            /**
             * Detects clients that have stopped sending pings and schedules disconnection handling.
             */
            @Override
            public void run() {
                try {
                    List<VirtualView> disconnectedClients = checkPingTimeouts();

                    if (!disconnectedClients.isEmpty()) {
                        System.out.println("[SERVER_APP] Timed-out clients: "
                                + disconnectedClients.size());

                        List<VirtualView> snapshot = List.copyOf(disconnectedClients);
                        disconnectionExecutor.submit(() -> handleClientDisconnection(snapshot));
                    }

                } catch (Exception e) {
                    System.out.println("[PING] Error alive checker: " + e.getMessage());
                }
            }
        }, 0, 1500);

        System.out.println("[PING] Alive checker started");
    }

    /**
     * Stops the current heartbeat checker and creates a new timer for future sessions.
     */
    private void stopAliveChecker() {
        try {
            aliveChecker.cancel();
        } catch (Exception ignored) {}

        aliveChecker = new Timer(true);
        aliveCheckerStarted = false;

        System.out.println("[PING] Alive checker stopped");
    }

    /**
     * Finds clients whose latest ping is older than the configured timeout.
     * <p>
     * Timed-out clients are removed immediately from the ping map to prevent the
     * timer from scheduling the same disconnection repeatedly before it is processed.
     *
     * @return client endpoints considered disconnected
     */
    private List<VirtualView> checkPingTimeouts() {
        List<VirtualView> disconnectedClients = new ArrayList<>();
        long now = System.currentTimeMillis();

        synchronized (lastPingByClient) {
            if(!lastPingByClient.isEmpty()){
                var iterator = lastPingByClient.entrySet().iterator();

                while (iterator.hasNext()) {
                    Map.Entry<VirtualView, Long> entry = iterator.next();

                    VirtualView client = entry.getKey();
                    long lastPing = entry.getValue();
                    long elapsed = now - lastPing;

                    if (elapsed > PING_TIMEOUT_MS) {
                        disconnectedClients.add(client);
                        iterator.remove();
                    }
                }
            }
        }
        return disconnectedClients;
    }


    /**
     * Handles clients detected as disconnected by the heartbeat checker.
     * <p>
     * A game-client disconnection crashes the active game, notifies remaining clients,
     * disconnects lobby clients, and resets the server. A lobby-only disconnection
     * only removes that lobby session.
     *
     * @param disconnectedClients disconnected client endpoints detected by timeout
     */
    private void handleClientDisconnection(List<VirtualView> disconnectedClients) {
        if (disconnectedClients == null || disconnectedClients.isEmpty()) {
            return;
        }
        List<String> disconnectedPlayersId = new ArrayList<>();
        List<String> sessionDisconnectedIds = new ArrayList<>();

        synchronized (gameClients) {
            for (Map.Entry<String, VirtualView> entry : gameClients.entrySet()) {
                if (disconnectedClients.contains(entry.getValue())) {
                    disconnectedPlayersId.add(entry.getKey());
                }
            }
        }
        synchronized (lobbyClients) {
            for (Map.Entry<String, VirtualView> entry : lobbyClients.entrySet()) {
                if (disconnectedClients.contains(entry.getValue())) {
                    sessionDisconnectedIds.add(entry.getKey());
                }
            }
        }
        if (!disconnectedPlayersId.isEmpty()) {
            System.out.println("[SERVER_APP] Disconnected game clients: " + disconnectedPlayersId);

            synchronized (gameClients) {
                for (String id : disconnectedPlayersId) {
                    gameClients.remove(id);
                }
            }

            synchronized (lobbyClients) {
                for (String sid : sessionDisconnectedIds) {
                    lobbyClients.remove(sid);
                }
            }

            for (VirtualView crashed : disconnectedClients) {
                try {
                    crashed.close();
                } catch (Exception ignored) {}
            }

            if (!this.isGameCrashed.get()) {
                MessageToClient message;
                synchronized (gameLock) {
                    message = controller.handleGameCrashed();
                }

                if (message != null) {
                    this.isGameCrashed.set(true);
                    message.deliver(null, this);
                    MessageToClient messageToLobby = new QuitLobbyMessage("Uno o più giocatori hanno chiesto di uscire dal game, sarai disconnesso...",true);
                    messageToLobby.deliver(null, this);
                }
            }
            resetServer();
            return;
        }

        if (!sessionDisconnectedIds.isEmpty()) {
            System.out.println("[SERVER_APP] Disconnected lobby clients: " + sessionDisconnectedIds);

            synchronized (lobbyClients) {
                for (String sid : sessionDisconnectedIds) {
                    lobbyClients.remove(sid);
                }
            }
            synchronized (lastPingByClient) {
                System.out.println("[SERVER_APP] Disconnected clients in lastPingByClient: " + lastPingByClient);
                if(lastPingByClient.isEmpty()) {
                    stopAliveChecker();
                }
            }
            for (VirtualView client : disconnectedClients) {
                try {
                    client.close();
                } catch (Exception ignored) {}
            }
        }
    }

    /**
     * Broadcasts the latest lobby snapshot to clients that are still waiting in the lobby.
     */
    private void broadcastLobbyStatus() {
        MessageToClient lobbyMessage =
                new LobbyStatusMessage(controller.getLobbyView(), "Lobby aggiornata.");

        broadcastToLobby(lobbyMessage);
    }

    /**
     * Sends a message to a single lobby client identified by its temporary session id.
     *
     * @param sessionId lobby session identifier
     * @param message   message to deliver
     */
    @Override
    public void sendToSession(String sessionId, MessageToClient message) {
        VirtualView client;

        synchronized (lobbyClients) {
            client = lobbyClients.get(sessionId);
        }

        if (client == null) {
            System.out.println("[SERVER_APP] ERROR: lobby session not found: " + sessionId);
            return;
        }

        try {
            client.onMessage(message);
        } catch (Exception e) {
            System.out.println("[SERVER_APP] Lobby client unreachable: " + sessionId
                    + ", error: " + e.getMessage());
        }
    }

    /**
     * Broadcasts a message to all clients currently registered in the lobby.
     *
     * @param message message to deliver to lobby clients
     */
    @Override
    public void broadcastToLobby(MessageToClient message) {
        Map<String, VirtualView> copy;

        synchronized (lobbyClients) {
            copy = new HashMap<>(lobbyClients);
        }

        for (Map.Entry<String, VirtualView> entry : copy.entrySet()) {
            String sessionId = entry.getKey();
            VirtualView client = entry.getValue();

            try {
                client.onMessage(message);
            } catch (Exception e) {
                System.out.println("[SERVER_APP] Lobby client unreachable during broadcast: "
                        + sessionId
                        + ", error: "
                        + e.getMessage());

            }
        }
    }

